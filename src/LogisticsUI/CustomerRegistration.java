    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        String name = custName.getText(), contact = contactPhone.getText(), address = custAddr.getText();
        Customer customer = new Customer(CSVParser.getLatestID("src/CSVFiles/customers.csv") + 1, name, contact, address);
        CSVParser.saveEntry(customer.toCSVFormat(), "src/CSVFiles/customers.csv");
        this.dispose();
        new CustomerDashboard(customer);
    }//GEN-LAST:event_registerActionPerformed

